// ── Core i18n engine ────────────────────────────────────────────────────────
function getLang() {
  return localStorage.getItem('jyanu_lang') || 'en';
}

function setLang(lang) {
  localStorage.setItem('jyanu_lang', lang);
  applyLang(lang);
  updateToggleBtn(lang);
  document.documentElement.lang = lang === 'rw' ? 'rw' : 'en';
}

function applyLang(lang) {
  const t = translations[lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = t[key];
      } else {
        el.textContent = t[key];
      }
    }
  });
  // placeholder attributes
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (t[key] !== undefined) el.placeholder = t[key];
  });
}

function updateToggleBtn(lang) {
  const btn = document.getElementById('langToggleBtn');
  if (!btn) return;
  btn.textContent = lang === 'en' ? '🇷🇼 Kinyarwanda' : '🇬🇧 English';
}

function toggleLang() {
  const current = getLang();
  setLang(current === 'en' ? 'rw' : 'en');
}

// ── Nav language toggle button HTML ─────────────────────────────────────────
function injectLangButton() {
  const nav = document.querySelector('nav ul');
  if (!nav) return;
  const li = document.createElement('li');
  li.innerHTML = `<button id="langToggleBtn" onclick="toggleLang()" style="
    background: var(--gold);
    color: #0f1419;
    border: none;
    padding: 0.35rem 0.9rem;
    border-radius: 20px;
    font-weight: 700;
    font-size: 0.8rem;
    cursor: pointer;
    letter-spacing: 0.5px;
    transition: all 0.3s ease;
    font-family: 'Montserrat', sans-serif;
    white-space: nowrap;
  " onmouseover="this.style.background='#fff';this.style.color='#1a4d2e'"
     onmouseout="this.style.background='var(--gold)';this.style.color='#0f1419'">
    🇷🇼 Kinyarwanda
  </button>`;
  nav.appendChild(li);
}

// ── Auto-init on page load ───────────────────────────────────────────────────
document.addEventListener('DOMContentLoaded', () => {
  injectLangButton();
  const lang = getLang();
  applyLang(lang);
  updateToggleBtn(lang);
  document.documentElement.lang = lang === 'rw' ? 'rw' : 'en';
});
